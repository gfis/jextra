      SUBROUTINE LRDUMP
C     PRINT THE PARSING TABLE READABLY
C     GF 11.07.1980 INCLUDES FIRST
C     GF 12.07.1980 : PRINT 'BUCS' AND 'CODS'
C
      INCLUDE 'PARS.F'
      INCLUDE 'BUCS.F'
      INCLUDE 'CODS.F'
      INCLUDE 'STAS.F'
      INCLUDE 'SYMS.F'
      INTEGER*2 PARASK
      INTEGER*2 I,J
     = ,MSTA     ! NUMBER OF THE LAST STATE,BE DUMPED
C
      CALL SETSIN
      MSTA = FNUM - 1
      IF (MSTA .LT. STAHIB) MSTA = STAHIB - 1
      ! MSTA = MIN(FNUM-1,STAHIB-1)
      DO 1 I = PARASK('OUTSTA',1,6,0),MSTA
        CALL OUTSTA(I)
1     CONTINUE ! ALL STATES
6     CONTINUE
      CALL OUTSYM(0)
      IF (PARASK('BUCS',1,4,0) .EQ. 0) GOTO 2
        DO 3 I=1,BUCHIB
          CALL ZZWI (I,3)
          CALL ZZWC ('. BUC: ',1,7,0)
          CALL OUTBUC(BUCKET(I))
          CALL ZZWS (0)
3       CONTINUE
        CALL OUTBUC (SPEH)
        CALL ZZWS (0)
2     CONTINUE
      IF (PARASK('CODS',1,4,0) .EQ. 0) GOTO 4
        WRITE(UPRI,5) (CODTAB(I),I-1,I=33,CODHIB)
5         FORMAT(8(4X,I2,A2))
4     CONTINUE
      RETURN! LRDUMP
      END
